## Declaraciones 

# = 
variable = "Spam"
print(variable)

# asignación de tupla (posicional)
# Funciones que nos retornan mas de dos valores 

invierno, verano = 'frio', 'calor'
print(invierno)
print(verano)

# asignación por listas 
[invierno, verano] = ['frio', 'calor']
print(invierno)
print(verano)

# secuencia  
a, b, c, d = 'pato'
print(a)
print(b)

# packing - desempaquetado de listas 
a, *b = 'spam'
print(a)
print(b)

a, *b, c = 'murcielago'
print(a)
print(b)
print(c)

# multiple 
a = b = "spam"
print(a)
print(b)

# incrementador 
spam=1
print(spam)
spam+=3
print(spam)

